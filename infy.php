<h3 style="text-align:justify"><strong>What's the Eligibility criteria for InfyTQ?</strong></h3>

<p style="text-align:justify">To register for InfyTQ, you should either be a B.E., B. Tech, M.E., M. Tech., M.Sc., MCA, or an MCM student graduating in 2022. There is no percentage criteria for InfyTQ. Student from any college, any branch/stream is eligible for InfyTQ.</p>

<hr>
<h3 style="text-align:justify"><strong>What is the Examination process of InfyTQ</strong>&nbsp;<strong>?</strong></h3>

<p>Watch the detailed InfyTQ paper pattern and InfyTQ selection process&nbsp;analysis. Understand the paper pattern to prepare better for the InfyTQ examination.&nbsp;</p>

<p style="text-align:justify"><strong><iframe allow="accelerometer; autoplay; clipboard-write; encrypted-media; gyroscope; picture-in-picture" allowfullscreen="" frameborder="0" height="315" src="https://www.youtube.com/embed/Nwzgxtd1rVk" width="390"></iframe></strong></p>

<p style="text-align:justify">Infosys Certification comprises three examination rounds:</p>

<ul>
	<li style="text-align:justify"><strong>Qualifying Round&nbsp;</strong>– We will test your knowledge in programming and database management along with your aptitude in this round.</li>
	<li style="text-align:justify"><strong>Final Round&nbsp;</strong>– If you clear the qualifying round, you will be required to take the final Infosys Certification examination, which will assess your in-depth programming and database management skills through objective and hands-on questions. If you score 65% or more in the final round, you will receive a certificate of being an ‘Infosys Certified Software Programmer’. You will also receive an opportunity to appear in a behavioral interview for the Systems Engineer role at Infosys.</li>
	<li style="text-align:justify"><strong>Advantage Round&nbsp;</strong>– This is an optional round for Infosys certified software programmers to gain an advantage and win a job opportunity for niche technical roles of&nbsp;Power Programmer&nbsp;and Systems Engineer Specialist at Infosys. If you decide to go through this round, you will have to take a test that will assess your programming and other technical skills. If you clear the round, you will receive a pre-placement interview opportunity for the&nbsp;Power Programmer&nbsp;and Systems Engineer Specialist roles. It will be a virtual interview that will assess your technical and behavioral skills.</li>
	<li style="text-align:justify"><strong>Note:</strong>&nbsp;If you are a B.E./B. Tech/M.E./M.Tech student graduating in 2022 who has also registered for&nbsp;HackWithInfy&nbsp;and if you choose to appear in the Advantage Round, then your performance in this round will be considered to determine your selection for HackWithInfy final round.</li>
</ul>

<h3 style="text-align:justify"><strong>InfyTQ Test Pattern &amp; Exam Date</strong></h3>

<p><strong>Date: 2nd &amp; 3rd Apri</strong></p>

<p style="text-align:justify"><strong>Qualifying Round (40 questions – 60 mins)</strong></p>

<ul>
	<li style="text-align:justify">Java or Python(MCQ): 20 questions</li>
	<li style="text-align:justify">Database Management Systems(MCQ): 10 questions</li>
	<li style="text-align:justify">Aptitude(mcq): 10 questions</li>
	<li style="text-align:justify">Marking Scheme: +1 for correct and -0.25 for incorrect answer</li>
</ul>

<p style="text-align:justify"><strong>Final&nbsp;Round (3 hours)</strong></p>

<ul>
	<li style="text-align:justify">Java or Python (hands-on): 2questions</li>
	<li style="text-align:justify">Java or Python (MCQ): 10 questions</li>
	<li style="text-align:justify">Database Management Systems (MCQ): 10 questions</li>
	<li style="text-align:justify">Marking Scheme: No negative marking</li>
</ul>

<p style="text-align:justify">If you score 65% or above in this round, you will become an ‘Infosys Certified Software Programmer’. You will also receive an opportunity to appear in a behavioral interview for the Systems Engineer role at Infosys.</p>

<p style="text-align:justify"><strong>Advantage Round (3 hours)</strong></p>

<ul>
	<li style="text-align:justify">Programming Questions with varying level of difficulty (3 questions)</li>
	<li style="text-align:justify">Each question will have a different weightage of marks. Roughly around 225 marks</li>
</ul>

<p style="text-align:justify">Students who cleared the Advantage Round will get a pre-replacement interview opportunity for either a Power Programmer or a Systems Engineer Specialist role based on their performance in the Advantage Round.</p>

<p style="text-align:justify">The interview for these roles will assess the technical and behavioral skills of the candidate.</p>

<h3 style="text-align:justify"><strong>Frequently Asked Questions(FAQs) for InfyTQ</strong></h3>

<p style="text-align:justify"><strong>1) Is there a minimum percentage required in 10th/12th/graduation/post-graduation to register for Infosys Certification?</strong></p>

<p style="text-align:justify">No, you do not require a minimum percentage to register for Infosys Certification.</p>

<p style="text-align:justify"><strong>2) Do I need to complete the learning journey on InfyTQ before taking the Infosys Certification?</strong></p>

<p style="text-align:justify">If you think you are competent enough to take the examination without completing the recommended learning journey, you may directly take the Infosys Certification.</p>

<p style="text-align:justify"><strong>3) How to Prepare for InfyTQ?</strong></p>

<p style="text-align:justify">You can prepare for InfyTQ using the free learning videos by Talent Battle on the YouTube channel.</p>

<p style="text-align:justify"><strong>4) Will the Test Rounds be Online or Offline?</strong></p>

<p style="text-align:justify">The test rounds will be online</p>

<p style="text-align:justify"><strong>5) How many times can I give InfyTQ exam?</strong></p>

<p style="text-align:justify">You can reappear for the assessment one month after your first attempt and 3 months post your second attempt.</p>

