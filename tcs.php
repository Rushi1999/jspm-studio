<p style="text-align:justify"><span style="color:#E74C3C"><strong>TCS NQT 2021 Eligibility Criteria: (Hiring for 2021 Done in October 2020)</strong></span></p>

<p><strong>PERCENTAGE</strong></p>

<p>An applicant must have minimum 60% or 6 CGPA throughout academics and an aggregate of 60% or 6.00 CGPA in the highest qualification till the semester for which results have been declared.</p>

<p><strong>BACKLOGS / ARREARS / ATKT</strong></p>

<p>The applicant must not have more than&nbsp;<strong>1</strong>&nbsp;active backlog/arrear/ATKT while appearing for the TCS Selection process.</p>

<p>If selected, the student while joining TCS must have cleared all pending backlogs/arrears/ATKT within the course duration as stipulated by the University failing which joining will not be permitted.</p>

<p><strong>GAP / BREAK IN EDUCATION</strong></p>

<p>It is mandatory to declare academic gaps. Overall break should not exceed 24 months. Any exceptions are permissible only for valid reasons subject to a satisfactory check of relevant document(s) at the time of test/interview/onboarding</p>

<p><strong>AGE</strong></p>

<p>An applicant should be between 18 to 28 years of age to participate in the TCS Campus Hiring process.</p>

<p><strong>COURSE TYPES</strong></p>

<p>Only full-time courses will be considered (part-time / correspondence courses will not be considered).</p>

<p>Applicants with Secondary and / or Senior Secondary course from NIOS (National Institute of Open Schooling) are eligible to apply, if the other courses are full time.</p>

<p><strong>EXTENDED EDUCATION</strong></p>

<p>Applicants with extended education in the highest qualification are not eligible. Any exceptions are permissible only for valid reasons subject to the satisfactory check of the relevant document(s) at the time of test/interview/onboarding.</p>

