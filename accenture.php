<h4><strong><u>Communication Assesment</u></strong></h4>

<p style="text-align:justify">This being the first round of the Accenture online test, consists of 6 sections in total. It has multiple questions in audio form. Note no text will be displayed for the same (except for the first section). You will be given 20-30 seconds to answer the questions orally.</p>

<p style="text-align:justify"><strong><u>Section 1 : Reading</u></strong></p>

<p style="text-align:justify">There will be 8 sentences displayed on the screen (in text form). You will have to read each of it aloud in the window of 20-30 seconds. This round focuses on an individual’s reading ability.</p>

<p style="text-align:justify"><strong><u>Section 2 : Listening</u></strong></p>

<p style="text-align:justify">This rounded consists of 16 audio sentences. All you need to do is listen to these sentences carefully and repeat them out loud. The score depends on how well an individual listens and replicates these sentences.</p>

<p style="text-align:justify"><strong><u>Section 3 : Question &amp; Answer</u></strong></p>

<p style="text-align:justify">In this section, you will be asked with 24 simple questions orally (no text will be displayed). Each question should be answered with word or two.</p>

<p style="text-align:justify"><strong>For example :</strong> Question – What is the color of mango?</p>

<p style="text-align:justify">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; &nbsp;&nbsp; Answer – Yellow</p>

<p style="text-align:justify"><strong><u>Section 4 : Jumbled Sentences</u></strong></p>

<p style="text-align:justify">In this section, you will be given with 10 jumbled sentences &amp; you have to arrange them properly to form a meaningful sentence.</p>

<p style="text-align:justify"><strong><u>Section 5 : Retelling A Story</u></strong></p>

<p style="text-align:justify">In this round you will be narrated with a short story and all you have to do is retell it. As soon as the story is narrated to you, you will hear a beep after which you will have to retell the story within 30 seconds, followed by another beep indicating start of the next story to be narrated.</p>

