<p>The <strong>Aptitude Section</strong> has Divisibility, Numbers, Decimal Fractions &amp; Powers, Profit and loss, Inverse, Time, Speed and Distance, Logarithms, Permutation and Combinations, Simple and Compound Interest, Probability.</p>

<p>The <strong>Logical Reasoning</strong> <strong>Section</strong> has Coding Deductive Logic, Blood Relations, Directional Sense, Objective Reasoning, Selection decision tables, Analogy and Classification, Coding pattern&amp; Number series, Logical word sequence, Data sufficiency.</p>

<p>The <strong>Verbal Section</strong> has Synonyms, Antonyms, Contextual Vocabulary, Error Identification, Sentence Improvement &amp; Construction, Subject-Verb Agreement, Tenses &amp; Articles, Preposition &amp; Conjunctions, Speech &amp; Voices, Inferential and Literal Comprehension, Comprehension ordering.</p>

<p>The <strong>Versant Test</strong> is one of the rounds conducted by Deloitte in a few colleges only. It examines an individual’s reading skills, listening skills &amp; analyzing skills. The test is conducted with the help of IVR (Interactive Voice Response). In it one must either listen to a particular clip played by the IVR and answer questions related to it or the individual could be asked to fill or correct the grammatical mistakes in the clip. The individual can also be asked to read aloud the given text on the screen or speak upon any given topics without grammatical mistakes or any error in the sentence formation.</p>

<p>&nbsp;</p>

