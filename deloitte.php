<p>Deloitte can conduct Technical &amp; HR interview within the same round. To clear technical problems, you should be prepared with Operating Systems, Computer Networks and database. Students will be expected to write codes in the interview. You should also be prepared with your resume. You may be asked puzzles in this round. The key to crack the interview is clearing your basics &amp; being good with your communication skills as well.</p>

