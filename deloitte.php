<p style="text-align:justify">(A) However, now that the Texan oil lines are in place and the regime is now being headed by an ex-consultant for an American oil company, the country is well on its way back to anarchy and internal disharmony.</p>

<p style="text-align:justify">(B) Of course, Osama could not be caught</p>

