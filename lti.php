<p><strong><u>1. Online Test</u></strong></p>

<p>The online test has the following sections :</p>

<ul>
	<li><strong>Psychometric</strong> (86 Questions, 20 minutes)</li>
	<li><strong>Quantitative Aptitude</strong> (10 Questions, 10 Minutes) - Algebra, Time &amp; Work, Time Speed &amp; Distance, Arithmetic, Percentages, Profit &amp; Loss, Geometry, etc.</li>
	<li><strong>Logical Reasoning</strong> (10 Questions, 10 Minutes) - Data interpretation, puzzles, coding and decoding, pie charts, line graphs, blood relation, etc.</li>
	<li><strong>Verbal Ability</strong> (10 Questions, 10 Minutes) - Short reading comprehension, passages, sentence completion, synonyms, paragraph completion, vocabulary, etc.</li>
	<li><strong>Technical MCQs</strong> (40 Questions, 40 Minutes)</li>
	<li><strong>Paragraph Writing</strong> (1 Question, 10 Minutes)</li>
</ul>

<p>There is no negative marking and students can attempt the test in any order of sections they want.</p>

<p>For the&nbsp;<strong>5 LPA package</strong>, there's an&nbsp;<strong>additional Coding Round</strong>&nbsp;for&nbsp;<strong>45 minute</strong>s with&nbsp;<strong>1 question</strong>.</p>

<ul>
	<li>It tests students&nbsp;ability to do basic programming.</li>
	<li>Students can choose between C/ C++/ Java and Python</li>
</ul>

<p><strong><u>3. Technical Interview</u></strong></p>

<p>This round tests the fundamental knowledge in computer programming of the candidate. .Be thorough with some of the programs such as (palindrome, Fibonacci, prime, Swap of 2 without using 3rd variable, Greatest of 3 numbers)..and be ready with your internships and projects. Majority times technical and HR interview rounds are combined. Many of the technical questions are either related to C/C++/JAVA, DS, OS or your core subjects, etc.</p>

<p><strong><u>4. HR Interview</u></strong></p>

<p>Majority times technical and HR interview rounds are combined. The candidate is asked questions related to my personal life, such as my hobbies, interests, strengths, and weaknesses, willingness to relocate, etc.</p>

